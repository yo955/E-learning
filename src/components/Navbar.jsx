const pages = [];

// eslint-disable-next-line react/prop-types
function Navbar({ setMymode }) {
  const theme = useTheme();
  const [anchorElNav, setAnchorElNav] = useState(null);
  const appBarRef = useRef(null);

  const handleOpenNavMenu = (event) => {
    setAnchorElNav(event.currentTarget);
  };

  const handleCloseNavMenu = () => {
    setAnchorElNav(null);
  };

  useEffect(() => {
    gsap.fromTo(
      appBarRef.current,
      { y: -100, opacity: 0 },
      {
        y: 0,
        opacity: 1,
        duration: 1,
        ease: "power2.out",
      }
    );

    ScrollTrigger.create({
      trigger: appBarRef.current,
      start: "top top",
      end: "bottom bottom",
      scrub: true,
      markers: false,
      onUpdate: (self) => {
        gsap.to(appBarRef.current, {
          y: -self.progress * 100,
          opacity: 1 - self.progress,
          ease: "power2.in",
        });
      },
    });
  }, []);

  return (
    <AppBar
      position="fixed"
      sx={{ top: 0, left: 0, right: 0, marginBottom: "2rem", padding: "5px 20px" }}
      ref={appBarRef}
      className="container"
    >
      <Container maxWidth="xl">
        <Toolbar disableGutters style={{ padding: "0.3rem 0.7rem" }}>
          <AdbIcon sx={{ display: { xs: "none", md: "flex" }, mr: 1 }} />
          <Typography
            variant="h6"
            noWrap
            component="a"
            href="#app-bar-with-responsive-menu"
            sx={{
              mr: 2,
              display: { xs: "none", md: "flex" },
              fontFamily: "monospace",
              fontWeight: 700,
              letterSpacing: ".3rem",
              color: "inherit",
              textDecoration: "none",
            }}
          >
            RYIAN
          </Typography>

          <Box sx={{ flexGrow: 1, display: { xs: "flex", md: "none" } }}>
            <IconButton
              size="large"
              aria-label="account of current user"
              aria-controls="menu-appbar"
              aria-haspopup="true"
              onClick={handleOpenNavMenu}
              color="inherit"
            >
              <MenuIcon />
            </IconButton>
            <Menu
              id="menu-appbar"
              anchorEl={anchorElNav}
              anchorOrigin={{
                vertical: "bottom",
                horizontal: "left",
              }}
              keepMounted
              transformOrigin={{
                vertical: "top",
                horizontal: "left",
              }}
              open={Boolean(anchorElNav)}
              onClose={handleCloseNavMenu}
              sx={{
                display: { xs: "block", md: "none" },
              }}
            >
              <div className="container">
                <Button
                  variant="contained"
                  style={{ marginTop: "5px" }}
                  className="btn-auth"
                >
                  سجل دخولك
                  <AccessibilityIcon />
                </Button>
                <Button
                  variant="contained"
                  style={{ marginTop: "10px" }}
                  className="btn-auth"
                >
                  اعمل حساب جديد
                  <LoginIcon />
                </Button>
              </div>

              {pages.map((page) => (
                <MenuItem key={page} onClick={handleCloseNavMenu}>
                  <Typography textAlign="center">{page}</Typography>
                </MenuItem>
              ))}
            </Menu>
          </Box>
          <AdbIcon sx={{ display: { xs: "flex", md: "none" }, mr: 1 }} />
          <Typography
            variant="h5"
            noWrap
            component="a"
            href="#app-bar-with-responsive-menu"
            sx={{
              mr: 2,
              display: { xs: "flex", md: "none" },
              flexGrow: 1,
              fontFamily: "monospace",
              fontWeight: 700,
              letterSpacing: ".3rem",
              color: "inherit",
              textDecoration: "none",
            }}
          >
            RYIAN
          </Typography>

          <IconButton
            sx={{ ml: 1 }}
            onClick={() => {
              setMymode(theme.palette.mode === "light" ? "dark" : "light");
            }}
            color="inherit"
          >
            {theme.palette.mode === "dark" ? (
              <FormControlLabel
                control={<MaterialUISwitch sx={{ m: 1 }} defaultChecked />}
              />
            ) : (
              <FormControlLabel
                control={<MaterialUISwitch sx={{ m: 1 }} defaultChecked />}
              />
            )}
          </IconButton>

          <Box sx={{ flexGrow: 1, display: { xs: "none", md: "flex" } }}>
            {pages.map((page) => (
              <Button
                key={page}
                onClick={handleCloseNavMenu}
                sx={{ my: 2, color: "white", display: "block" }}
              >
                {page}
              </Button>
            ))}
          </Box>
          <Search>
            <SearchIconWrapper>
              <SearchIcon />
            </SearchIconWrapper>
            <StyledInputBase
              placeholder="Search…"
              inputProps={{ "aria-label": "search" }}
            />
          </Search>
          <Box sx={{ flexGrow: 0 }} className="hidden">
            <Button
              variant="outlined"
              style={{ color: "#fff", borderColor: "#fff", marginLeft: "10px" }}
            >
              سجل دخولك
              <AccessibilityIcon />
            </Button>
            <Button variant="contained" style={{ marginLeft: "10px" }}>
              اعمل حساب جديد
              <LoginIcon />
            </Button>
          </Box>
        </Toolbar>
      </Container>
    </AppBar>
  );
}

export default Navbar;
